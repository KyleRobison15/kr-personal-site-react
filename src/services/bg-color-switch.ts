export const navBgSwitch = (colorMode: string) => {
  colorMode === "light" ? "gray.100" : "gray.900";
};
export const primaryBgSwitch = (colorMode: string) => {
  colorMode === "light" ? "gray.200" : "gray.800";
};
export const secondaryBgSwitch = (colorMode: string) => {
  colorMode === "light" ? "gray.50" : "gray.700";
};
